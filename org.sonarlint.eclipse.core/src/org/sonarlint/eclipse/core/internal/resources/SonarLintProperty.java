package org.sonarlint.eclipse.core.internal.resources;

public class SonarLintProperty {
  private String name;
  private String value;

  public SonarLintProperty(String name, String value) {
    this.name = name;
    this.value = value;
  }

  public SonarLintProperty(SonarLintProperty another) {
    this.name = another.name;
    this.value = another.value;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getValue() {
    return value;
  }

  public void setValue(String value) {
    this.value = value;
  }

  @Override
  public boolean equals(Object obj) {
    if (obj == null) {
      return false;
    }
    if (obj == this) {
      return true;
    }
    if (obj.getClass() != getClass()) {
      return false;
    }
    SonarLintProperty rhs = (SonarLintProperty) obj;
    return name.equals(rhs.name) && value.equals(rhs.value);
  }

  @Override
  public int hashCode() {
    return name.hashCode() + value.hashCode();
  }
}
